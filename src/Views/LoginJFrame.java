    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String ten = this.txtUName.getText().trim();
       String pw = this.txtPW.getText().trim();

        if (ten.length() == 0 || pw.length() == 0) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return;
        }

        try {
            if (pw.length() < 8) {
                JOptionPane.showMessageDialog(this, "Mật khẩu phải lớn hơn 8 ký tự");
                return;
            }
        } catch (Exception e) {
            e.printStackTrace();
            
        }

        Login l = new Login(ten, pw);

        try {
            if(this.loginService.timkiem(ten, pw)) {
                JOptionPane.showMessageDialog(this, "Login succesfully!!");
            } else {
                JOptionPane.showMessageDialog(this, "Login Failed!!");
            }

        } catch (Exception e) {
            e.printStackTrace();
           return;
        }
        
        int messageType = JOptionPane.QUESTION_MESSAGE;
        String[] options = {"Giảng Viên", "Cán Bộ Đào Tạo"};
        int code = JOptionPane.showOptionDialog(this, "Moi chon vai tro quan ly", "vai tro", JOptionPane.YES_NO_OPTION, messageType, null, options, null);
        
        if(code == 0){
            GradeJFrame gradeJFrame = new GradeJFrame();
            gradeJFrame.setVisible(true);

            JOptionPane.showMessageDialog(this, "Chào mừng đến với chức năng quản lý Điểm sinh viên!!!");
        } else if(code == 1){
            StudentsJFrame studentsJFrame = new StudentsJFrame();
            studentsJFrame.setVisible(true);

            JOptionPane.showMessageDialog(this, "Chào mừng đến với chức năng quản lý sinh viên!!!");
        }

    }//GEN-LAST:event_btnLoginActionPerformed

